import UIKit

extension UIFont {
    
    static let largeTitle: UIFont = .systemFont(ofSize: 17)
    static let mediumTitle: UIFont = .systemFont(ofSize: 16)
    static let large: UIFont = .systemFont(ofSize: 15)
    static let medium: UIFont = .systemFont(ofSize: 14)
    static let small: UIFont = .systemFont(ofSize: 13)
    
    static let largeTitleBold: UIFont = .boldSystemFont(ofSize: 17)
    static let mediumTitleBold: UIFont = .boldSystemFont(ofSize: 16)
    static let largeBold: UIFont = .boldSystemFont(ofSize: 15)
    static let mediumBold: UIFont = .boldSystemFont(ofSize: 14)
    static let smallBold: UIFont = .boldSystemFont(ofSize: 13)
}
